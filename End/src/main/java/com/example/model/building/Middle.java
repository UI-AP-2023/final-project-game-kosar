package com.example.model.building;

public class Middle {
    private double X;
    private double Y;
    public Middle(double x, double y){
        this.X = x ;
        this.Y = y;
    }

    public double getX() {
        return X;
    }

    public double getY() {
        return Y;
    }

    public void setX(double x) {
        X = x;
    }

    public void setY(double y) {
        Y = y;
    }
}
